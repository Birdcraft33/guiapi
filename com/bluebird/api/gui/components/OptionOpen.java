package com.bluebird.api.gui.components;

/**
 * <h2>general</h2>
 * The OptionOpen set the type an {@link Option}Component (also the {@link YesNoOption}) will open
 * 
 * <h2>types</h2>
 * <dt>underInventory</dt>
 * UnderInventory means that the Inventory will be one line bigger and in this new Line are all options<br>
 * <dt>newInventory</dt>
 * NewInventory means that a new Inventory will be open for the options
 * 
 * @author Birdcraft33
 * @category OpenType
 * @version 1.0
 * @since 1.0
 * @extends no
 * @initialize no
 */
public enum OptionOpen {

	underInventory, newInventory;
	
}
